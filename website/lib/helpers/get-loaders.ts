// Create a function to retrieve loaders by category
export const getLoadersByCategory = memoizer((category: string): LoaderProps[] => {
    switch (category) {
        case 'Arrows':
        case 'Bars':
        case 'Circles':
        case 'Dots':
        case 'Emojis':
        case 'Lines':
        case 'Numbers':
        case 'Squares':
        case 'Symbols':
        case 'Togglers':
            return getLoaders().filter(loader => loader.category === category);
        default:
            return [];
    }
});

// Create a function to retrieve a loader by name
export const getLoaderByName = memoizer((name: string): LoaderProps | undefined => {
    const loader = getLoaders().find(loader => loader.name === name);
    if (!loader) {
        throw new Error(`Loader '${name}' not found`);
    }
    return loader;
});
